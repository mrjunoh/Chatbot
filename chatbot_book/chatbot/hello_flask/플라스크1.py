from flask import Flask #-1
app = Flask(__name__) #-2

@app.route('/') #-3
def hello():
    return 'hello flask'

if __name__ =='__main__': #-4
    app.run() #app.run(host='127.0.0.1', port='5000')이 생략되어있음
    
