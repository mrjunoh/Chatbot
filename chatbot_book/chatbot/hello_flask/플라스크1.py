#flask 웹 애플리케이션을 시작하기 위해 flask 인스턴스 객체를 생성함
#이때 flask클래스의 생성자로 현재 실행되는 애플리케이션 모듈명을 전달해야함
#파이썬 전역 변수인 __name__을 사용하면 현재 실행되는 애플리케이션의 모듈명이 자동으로 들어감
#메인 모듈로 실행되기 때문에 __name__ 에는 __main__문자열이 저장되어 있다.

